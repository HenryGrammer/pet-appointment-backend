    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        try {
            $appointment = new Appointment;
            $appointment->frequency = $request->frequency;
            $appointment->start_date  = $request->startDate;
            $appointment->days = $request->day;
            $appointment->time = $request->time;
            $appointment->reason = $request->reason;
            $appointment->save();
    
            return response()->json(['status' => 201, 'msg' => 'Successfully Saved']);
        } catch (\Throwable $th) {
            return response()->json(['status' => 400, 'msg' => $th->getMessage()]);
        }


    }

